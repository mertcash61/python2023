# İki sayıyı topla
sayi1 = 10
sayi2 = 15
toplam = sayi1 + sayi2
print("Topla:",toplam)

# Bir sayıyı diğerinden çıkar
fark = sayi1 - sayi2
print("Fark:",fark)

# İki sayıyı çarp
carpim = sayi1 * sayi2
print("Çarpım:",carpim)

# Bir sayıyı diğerine böl
bolum = sayi1 / sayi2
print("Bölüm:",bolum)

# Bir sayının kuvvetini al
us = sayi1 ** sayi2
print("Üs:",us)